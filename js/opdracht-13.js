  // Definieer de functie showSum met twee parameters genaamd num1 en num2
  function showSum(num1, num2) {
    // Bereken de som van num1 en num2
    const som = num1 + num2;

    // Print de som op de console
    console.log("De som van " + num1 + " en " + num2 + " is: " + som);
}

// Roep de functie showSum aan met voorbeeldgetallen
showSum(5, 7);