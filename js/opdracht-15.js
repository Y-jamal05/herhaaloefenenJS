 // Definieer de functie getProduct met twee parameters genaamd num1 en num2
 function getProduct(num1, num2) {
    // Bereken het product van num1 en num2 en retourneer de waarde
    return num1 * num2;
}

// Voorbeeld van het aanroepen van de functie getProduct met twee getallen
const resultaat = getProduct(4, 7);
console.log("Het product is: " + resultaat);